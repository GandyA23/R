# Vectores numéricos

# Se crea el vector x con la función c (concatenar)
x <- c(1, 2, 3)

# Imprime el valor de la variable x en consola
x

# Se crea el vector y con la función c (concatenar)
y <- c(4, 5, 6)

# Imprime el valor de la variable y en consola
y

# Imprime la concatenación de los vectores x, y
c(x, y)

# Otra manera de asignar valores a variables
# Se asigna el vector numérico a la variable z
# Una línea de código parecida es: z <- c(7, 8, 9)
assign("z", c(7, 8, 9))

# Imprime el valor de la variable z en consola
z

# Imprime la concatenación de los vectores x, y, z
c(x, y, z)
